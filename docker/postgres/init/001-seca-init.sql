CREATE TYPE user_role_type AS ENUM ('admin', 'user');
CREATE TYPE rag_mode_type AS ENUM ('rag_on', 'rag_off');
CREATE TYPE active_model_type AS ENUM ('gemini_freetier', 'local_8B', 'openai_4_mini', 'openrouter');
CREATE TYPE session_type AS ENUM ('Industry', 'Corporation', 'Team', 'Transaction', 'AI-Conversation', 'Misc');
CREATE TYPE conversation_role AS ENUM (
  'user',
  'snow_kb keywords',
  'snow_kb data',
  'snow_inc keywords',
  'snow_inc data',
  'rag_bid keywords',
  'rag_data',
  'upl data',
  'assistant',
  'system'
);
CREATE TYPE removed_flag_type AS ENUM ('IN', 'OUT');

CREATE TABLE users (
  user_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role user_role_type DEFAULT 'user',
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(255) NOT NULL,
  rag_mode rag_mode_type DEFAULT 'rag_off',
  first_nm VARCHAR(50) NOT NULL,
  last_nm VARCHAR(50) NOT NULL,
  address VARCHAR(255),
  phone VARCHAR(20),
  postal_cd VARCHAR(20),
  active_session_id INTEGER NULL,
  active_model active_model_type DEFAULT 'openai_4_mini',
  seedbelief TEXT,
  created_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
  updated_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE roles (
  role_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  role_desc VARCHAR(255) NOT NULL UNIQUE,
  created_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
  updated_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sessions (
  session_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_owner_user_id INTEGER NOT NULL,
  session_desc VARCHAR(255) NOT NULL,
  session_type session_type DEFAULT 'AI-Conversation',
  role_id INTEGER NULL,
  created_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
  updated_dttm TIMESTAMPTZ DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (session_owner_user_id) REFERENCES users(user_id) ON DELETE CASCADE,
  FOREIGN KEY (role_id) REFERENCES roles(role_id)
);

